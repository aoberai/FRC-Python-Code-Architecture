def color(x):

    if x == "red":
        return 0, 255, 100

    if x == "orange":
        return 2, 247, 87

    if x == "yellow":
        return 30, 255, 20

    if x == "green":
        return 70, 255, 20

    if x == "blue":
        return 85, 247, 20

    if x == "purple":
        return 180, 247, 20

    if x == "pink":
        return 0, 200, 100

    if x == "white":
        return 0, 75, 20

    if x == "off":
        return 0, 0, 0
